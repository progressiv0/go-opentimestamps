package core

import "golang.org/x/crypto/sha3"

// keccak256 computes the Keccak-256 (legacy, pre-SHA3) hash of data.
func keccak256(data []byte) []byte {
	h := sha3.NewLegacyKeccak256()
	h.Write(data)
	return h.Sum(nil)
}
